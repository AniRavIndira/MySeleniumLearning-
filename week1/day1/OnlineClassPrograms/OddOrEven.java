package week1.day1;

public class OddOrEven {

	// to check odd or even
	public static void main(String[] args) {
		System.out.println("Odd or Even...");
		int num = 15;
		if (num % 2 == 0) {
			System.out.println("Even number..." + num);
		} else {
			System.out.println("Odd number..." + num);
		}
	}

}
